// -------------------------------------------------------------------------
// 自宅外通学（下宿・一人暮らし）による「追加生活費」の参考差額
//
// 出典: 日本学生支援機構（JASSO）「令和6年度 学生生活調査」
//       大学（学部・昼間部）・全設置者平均・居住形態別の平均生活費。
//       https://www.jasso.go.jp/statistics/gakusei_chosa/2024.html
//
// 計算式（差額）:
//   自宅外通学による追加生活費
//     = 自宅外通学生（下宿・アパート・その他）の生活費 − 自宅生の生活費
//     = 1,256,700 − 500,800 = 755,900 円/年
//
// 重要（表現・二重計上の扱い）:
//   この 755,900 円は「住居・光熱費だけの差額」ではない。生活費には
//   食費・住居光熱費・保健衛生費・娯楽し好費・その他日常費（通信費含む）が含まれ、
//   その自宅外−自宅の差額である。よって「住居・光熱費のみ」「下宿代のみ」とは表現しない。
//   総合版の通常生活費は「子どもが自宅通学している家庭生活」を基準前提として扱い、
//   本値はそれを置換せず、大学在学期間の教育費イベントへ加算する形で二重計上を避ける。
//   これは厳密な家計差額ではなく、生活設計のための一般的な目安。
//
//   国公立／私立で学費は別に分けているため、自宅外通学の追加生活費は MVP では共通の参考額。
// -------------------------------------------------------------------------
